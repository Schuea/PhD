C
      IBEAM=ENER
      MINP=PMIN
C     PRINT 96, ENER
 96   FORMAT(T2,'ENER =',E10.4)  
C     PRINT 99, DELE,RANDOM,INDEX0
 99   FORMAT(T2,2F10.4,I5)
      RN=rand()
      DO 1 IP = MINP,IBEAM
C     PRINT 92, P_THETA_DIST(IP,70),IP, MINP, RN
 92   FORMAT('P,THETA(IP,70) = ',F10.4,2I5,F10.5)
      IF (P_THETA_DIST(IP,70).GT.RN) GO TO 2
    1 CONTINUE
